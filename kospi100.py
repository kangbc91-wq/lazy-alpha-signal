# 종목코드 세트 (빠른 조회용)
KOSPI_TOP100_CODES = set(KOSPI_TOP100.keys())


def is_in_top100(ticker: str) -> bool:
    """종목코드가 코스피 100 안에 있는지 확인"""
    return ticker.strip() in KOSPI_TOP100_CODES


def get_stock_name(ticker: str) -> str:
    """종목코드로 종목명 반환"""
    return KOSPI_TOP100.get(ticker.strip(), ticker)


def update_kospi100():
    """
    pykrx로 최신 코스피 시총 상위 100 종목 업데이트
    실행: python kospi100.py

    설치 필요: pip install pykrx
    """
    try:
        from pykrx import stock
        from datetime import datetime, timedelta

        today = datetime.today().strftime("%Y%m%d")
        yesterday = (datetime.today() - timedelta(days=1)).strftime("%Y%m%d")

        print(f"📡 KRX에서 코스피 시총 데이터 가져오는 중...")
        df = stock.get_market_cap_by_ticker(yesterday, market="KOSPI")
        df = df.sort_values("시가총액", ascending=False).head(100)

        print(f"\n✅ 코스피 시총 상위 100 종목 (기준일: {yesterday})")
        print("─" * 40)

        new_dict = {}
        for ticker, row in df.iterrows():
            name = stock.get_market_ticker_name(ticker)
            new_dict[ticker] = name
            print(f'    "{ticker}": "{name}",')

        print("\n📋 위 목록을 kospi100.py의 KOSPI_TOP100에 붙여넣으세요.")
        return new_dict

    except ImportError:
        print("❌ pykrx가 설치되지 않았습니다.")
        print("   설치: pip install pykrx")
    except Exception as e:
        print(f"❌ 업데이트 실패: {e}")
    return None


if __name__ == "__main__":
    print(f"현재 등록된 종목 수: {len(KOSPI_TOP100_CODES)}개")
    print("\n최신 목록으로 업데이트하려면:")
    update_kospi100()
